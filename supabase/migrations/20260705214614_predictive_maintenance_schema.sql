DROP POLICY IF EXISTS "snapshots_delete" ON sensor_snapshots;
CREATE POLICY "snapshots_delete" ON sensor_snapshots FOR DELETE TO authenticated USING (auth.uid() = user_id);

-- ============================================================
-- ALERTS
-- ============================================================
CREATE TABLE IF NOT EXISTS alerts (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  machine_id uuid NOT NULL REFERENCES machines(id) ON DELETE CASCADE,
  user_id uuid NOT NULL DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
  type text NOT NULL CHECK (type IN ('bearing_wear','overheating','abnormal_vibration','current_spike','rpm_anomaly')),
  severity text NOT NULL DEFAULT 'warning' CHECK (severity IN ('info','warning','critical')),
  message text NOT NULL,
  is_read boolean NOT NULL DEFAULT false,
  resolved_at timestamptz,
  created_at timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS alerts_machine_created ON alerts(machine_id, created_at DESC);
CREATE INDEX IF NOT EXISTS alerts_user_unread ON alerts(user_id, is_read, created_at DESC);


/*
# Predictive Maintenance System - Complete Schema

## Overview
Full schema for an AI-powered industrial predictive maintenance system.
Users can monitor multiple machines with multiple sensors, receive AI-generated
alerts, view predictions, and track maintenance history.

## Tables Created

### 1. machines
Stores the factory machines being monitored.
- id: UUID primary key
- user_id: Owner (defaults to auth.uid())
- name: Machine display name (e.g., "Compressor Unit 01")
- location: Physical location string
- status: 'online' | 'offline' | 'warning' | 'critical'
- description: Optional notes
- rms_min / rms_max: Vibration RMS thresholds (g)
- temp_min / temp_max: Temperature thresholds (°C)
- current_min / current_max: Current thresholds (A)
- created_at, updated_at

### 2. sensor_snapshots
Periodic (5s) averaged readings per machine. Used for trends.
- id: UUID primary key
- machine_id: FK to machines
- user_id: Owner
- temperature: °C
- vibration_rms: g (combined X/Y RMS)
- current: A
- rpm: revolutions per minute
- voltage: V
- recorded_at: timestamptz

### 3. alerts
Machine-level alerts and warnings generated by AI detection.
- id: UUID primary key
- machine_id: FK to machines
- user_id: Owner
- type: 'bearing_wear' | 'overheating' | 'abnormal_vibration' | 'current_spike' | 'rpm_anomaly'
- severity: 'info' | 'warning' | 'critical'
- message: Human-readable description
- is_read: Whether user has acknowledged
- resolved_at: When resolved (nullable)
- created_at

### 4. predictions
AI health assessment snapshots per machine.
- id: UUID primary key
- machine_id: FK to machines
- user_id: Owner
- health_score: 0-100 overall health percentage
- status: 'healthy' | 'warning' | 'critical'
- bearing_wear_pct: 0-100
- overheating_risk_pct: 0-100
- failure_risk_pct: 0-100
- rul_hours: Remaining Useful Life estimate in hours
- predicted_at

### 5. maintenance_logs
History of maintenance actions performed on machines.
- id: UUID primary key
- machine_id: FK to machines
- user_id: Owner
- action: Short action title
- notes: Detailed notes
- performed_by: Name/role of technician
- performed_at

### 6. machine_health
Latest health snapshot per machine (upserted on update).
- machine_id: PK (one row per machine)
- user_id: Owner
- rms_x, rms_y: Per-axis RMS (g)
- temperature: °C
- current: A
- rpm: RPM
- voltage: V
- health_score: 0-100
- status: health status string
- updated_at

## Security
- RLS enabled on all tables
- All policies scoped to authenticated users
- Each user can only access their own data (user_id = auth.uid())
- Separate policies for SELECT / INSERT / UPDATE / DELETE
*/

-- ============================================================
-- MACHINES
-- ============================================================
CREATE TABLE IF NOT EXISTS machines (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id uuid NOT NULL DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
  name text NOT NULL,
  location text NOT NULL DEFAULT '',
  description text NOT NULL DEFAULT '',
  status text NOT NULL DEFAULT 'online' CHECK (status IN ('online','offline','warning','critical')),
  rms_min numeric NOT NULL DEFAULT 0.5,
  rms_max numeric NOT NULL DEFAULT 3.0,
  temp_min numeric NOT NULL DEFAULT 20.0,
  temp_max numeric NOT NULL DEFAULT 85.0,
  current_min numeric NOT NULL DEFAULT 0.5,
  current_max numeric NOT NULL DEFAULT 5.0,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE machines ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "machines_select" ON machines;
CREATE POLICY "machines_select" ON machines FOR SELECT TO authenticated USING (auth.uid() = user_id);

DROP POLICY IF EXISTS "machines_insert" ON machines;
CREATE POLICY "machines_insert" ON machines FOR INSERT TO authenticated WITH CHECK (auth.uid() = user_id);

DROP POLICY IF EXISTS "machines_update" ON machines;
CREATE POLICY "machines_update" ON machines FOR UPDATE TO authenticated USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

DROP POLICY IF EXISTS "machines_delete" ON machines;
CREATE POLICY "machines_delete" ON machines FOR DELETE TO authenticated USING (auth.uid() = user_id);

-- ============================================================
-- SENSOR SNAPSHOTS
-- ============================================================
CREATE TABLE IF NOT EXISTS sensor_snapshots (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  machine_id uuid NOT NULL REFERENCES machines(id) ON DELETE CASCADE,
  user_id uuid NOT NULL DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
  temperature numeric NOT NULL DEFAULT 0,
  vibration_rms numeric NOT NULL DEFAULT 0,
  current numeric NOT NULL DEFAULT 0,
  rpm integer NOT NULL DEFAULT 0,
  voltage numeric NOT NULL DEFAULT 0,
  recorded_at timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS sensor_snapshots_machine_time ON sensor_snapshots(machine_id, recorded_at DESC);

ALTER TABLE sensor_snapshots ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "snapshots_select" ON sensor_snapshots;
